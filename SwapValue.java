public class SwapValue {
    public static void main(String[] args) {
        System.out.println("Swapping 2 Numbers");
        int a = 5;
        int temp;
        int b = 9;
        System.out.println("a = "+a);
        System.out.println("b = "+b);
        temp = a;
        a = b;
        b = temp;
        System.out.println("a = "+a);
        System.out.println("b = "+b);
        

    }
}
